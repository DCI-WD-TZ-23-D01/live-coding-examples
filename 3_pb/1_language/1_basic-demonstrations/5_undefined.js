// Undefined ist ein datentyp für nicht deklarierte werte, und null ist ein objekt, für einen wert der angegeben werden kann.
// https://codeburst.io/javascript-null-vs-undefined-20f955215a2

// undefined bedeutet, eine variable ist deklariert, hat aber keinen wert.

// null ist ein deklarierbarer wert, den man zu einer variable anfügen kann.

let demo1;

console.log(typeof demo1);

demo1 = 5;

console.log(typeof demo1);

const demo2 = null;

console.log(typeof demo2);

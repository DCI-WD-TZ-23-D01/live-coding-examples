// booleans
true;
false;

// Equal to operator: == (Equality)
    // TRUE: der erste wert ist gleich dem zweiten wert:
    9 == 9; // true

    // FALSE: der erste wert ist NICHT gleich dem zweiten wert:
    4 == 8; // false

    // TRUE: Der erste wert ist gleich dem zweiten wert:
    4 == "4"; // true

// Equal to by type and value: === (Identity)
    // TRUE: Der erste wert ist vom SELBEN TYP und hat den SELBEN WERT wie der zweite:
    6 === 6; // true

    // FALSE: Der erste wert ist ein ANDERER TYP als der ZWEITE, hat aber den selben wert:
    6 === "6";

    // FALSE: Der erste wert ist vom GLEICHEN TYP wie der zweite, hat aber einen anderen wert:
    6 === 5;

// Not equal operator: !==
    // TRUE: der erste wert ist nicht gleich dem zweiten wert:
    1 !== 2;

    // FALSE: der erste wert ist gleich dem zweiten wert:
    5 !== 5;

// Greater than operator: > 
    // TRUE: wert a ist größer als wert b
    5 > 4;

    // TRUE: wert a ist größer als wert b
    11 > 9;

    // FALSE: wert a ist nicht größer als wert b
    4 > 5;

// Lesser than operator: < 
    // TRUE: wert a ist weniger/kleiner als wert b
    3 < 6;

    // FALSE: wert a ist nicht weniger/kleiner als wert b
    3 < 2.5;

// Greater than or equal operator: >=
    // TRUE: wert a ist größer oder gleich wert b
    4 >= 4;

    // FALSE: wert a ist nicht gleich oder größer als wert b:
    20 >= 45;

// Lesser than or equal operator: <=
    // TRUE: wert a ist kleiner oder gleich wert b
    7 <= 8;

    // FALSE: wert a ist nicht kleiner oder gleich wert b
    5 <= 4;

// Inversion
    !true; // wird zu false
    !false; // wird zu true

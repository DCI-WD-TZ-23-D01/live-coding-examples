// https://developer.mozilla.org/en-US/docs/Learn/JavaScript/First_steps/Math

// Integer (ganze zahlen)
100;
5;
7;
31;

// Floats (kommanzahlen)
8.5;
7.6;
1.2;
3.5;

// da wir in englisch programmieren, sind kommazahlen per punkt getrennt, nicht per komma.

// Negative zahlen
-4;
-7.3;

// Plus: +
3 + 5;
3.5 + 8;

// Minus: -
8 - 4.2;
6 - 2;

// Multiplication: *
3 * 7;
8 * 4;
2 * 3.5;
1.2 * 2;

// Division: /
10 / 3;
6 / 5;
5 / 6;

// Remainder (In anderen sprachen oft auch 'Modulo' genannt.)
8 % 3; // ich kann 6 durch 3 teilen, es bleiben 2 übrig.
7 % 2; // ich kann 6 durch 2 teilen, bleibt 1 übrig.
11 % 3; // ich kann 9 durch 3 teilen, bleiben 2 übrig.

// Exponent (X multipliziert mit sich selber, Y mal)
5 ** 2; // 5²
4 ** 3; // 4³

// Operator precedence.
    1 + 8 / 4 + 5; // Javascript liest: 1 + 2 + 5;
    1 + (8 / 4) + 5;

// Wollen wir dies nicht, müssen wir die verschiedenen rechenschritte in klammern setzen
    (1 + 8) / (4 + 5);
